/**
Classe que possue as assinaturas dos metodos que devem ser implementados na 
DAOIMPL.
*/
public interface FracaoAnexoDAO{
   

   public abstract void incluir(FracaoAnexoTO fracaoAnexoTO) throws ExceptionPersistenciaPRO;
  
   public abstract void excluir(FracaoAnexoTO fracaoAnexoTO) throws ExceptionPersistenciaPRO;
  
   public abstract Collection download(FracaoAnexoTO FracaoAnexoTO) throws ExceptionPersistenciaPRO;
   
   public abstract int consultarMax(FracaoAnexoTO fracaoAnexoTO) throws ExceptionPersistenciaPRO;
}
