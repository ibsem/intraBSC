
package br.com.intraPRO.modelo;



/**
Classe que representa a transaferencia de dados entre as camadas.
*/
public class ParticipanteTO{
   private int anoCriacao;   
   private int codTarefa;
   private int codigo;
   private int codTipoParticipacao;
   private int codPapel;
   private int codUsuario;
   private int[] listaParticipante;
   private String login;
   private String nome;
   private String email;
   private String nomeUsuario;
   
   private int codNivel;/*Variavel utilizada para marcar a tarefa como negrito na arvore de tarefa.*/
   
   public ParticipanteTO(){
   }
	/**
	 * @return Returns the anoCriacao.
	 */
	public int getAnoCriacao() {
	    return anoCriacao;
	}
	/**
	 * @param anoCriacao The anoCriacao to set.
	 */
	public void setAnoCriacao(int anoCriacao) {
	    this.anoCriacao = anoCriacao;
	}
	
	/**
	 * @return Returns the codTarefa.
	 */
	public int getCodTarefa() {
	    return codTarefa;
	}
	/**
	 * @param codTarefa The codTarefa to set.
	 */
	public void setCodTarefa(int codTarefa) {
	    this.codTarefa = codTarefa;
	}
	
	/**
	 * @return Returns the codigo.
	 */
	public int getCodigo() {
	    return codigo;
	}
	/**
	 * @param codigo The codigo to set.
	 */
	public void setCodigo(int codigo) {
	    this.codigo = codigo;
	}
	
	/**
	 * @return Returns the codTipoParticipacao.
	 */
	public int getCodTipoParticipacao() {
	    return codTipoParticipacao;
	}
	/**
	 * @param codTipoParticipacao The codTipoParticipacao to set.
	 */
	public void setCodTipoParticipacao(int codTipoParticipacao) {
	    this.codTipoParticipacao = codTipoParticipacao;
	}
	/**
	 * @return Returns the codUsuario.
	 */
	public int getCodUsuario() {
	    return codUsuario;
	}
	/**
	 * @param codUsuario The codUsuario to set.
	 */
	public void setCodUsuario(int codUsuario) {
	    this.codUsuario = codUsuario;
	}
	public String nomeUsuario() {
	    return nomeUsuario;
	}
	public void setNomeUsuario(String nomeUsuario) {
		this.nomeUsuario = nomeUsuario;
	}
	public String getNomeParticipante() {
		return nomeUsuario;
	}
	
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	/**
	 * @return Returns the listaParticipante.
	 */
	public int[] getListaParticipante() {
		return listaParticipante;
	}
	/**
	 * @param listaParticipante The listaParticipante to set.
	 */
	public void setListaParticipante(int[] listaParticipante) {
		this.listaParticipante = listaParticipante;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getCodNivel() {
		return codNivel;
	}
	public void setCodNivel(int codNivel) {
		this.codNivel = codNivel;
	}
	public int getCodPapel() {
		return codPapel;
	}
	public void setCodPapel(int codPapel) {
		this.codPapel = codPapel;
	}

}
