package br.com.intraPRO.modelo;

import net.sf.jasperreports.engine.JasperReport;

public class RelatorioTO {
	
	private JasperReport jasperReport;
	private String transicao;
	private String baseDir;
	private int codDependencia;
	private String chaveSolicitante = "";
	private String chaveParticipante = "";
	private String nomeSolicitante = "";
	private String nomeParticipante = "";
	private String dataPrazo = "";


	public String getNomeParticipante() {
		return nomeParticipante;
	}

	public void setNomeParticipante(String nomeParticipante) {
		this.nomeParticipante = nomeParticipante;
	}

	public String getNomeSolicitante() {
		return nomeSolicitante;
	}

	public void setNomeSolicitante(String nomeSolicitante) {
		this.nomeSolicitante = nomeSolicitante;
	}

	public int getCodDependencia() {
		return codDependencia;
	}

	public void setCodDependencia(int codDependencia) {
		this.codDependencia = codDependencia;
	}

	public String getTransicao() {
		return transicao;
	}

	public void setTransicao(String transicao) {
		this.transicao = transicao;
	}

	public JasperReport getJasperReport() {
		return jasperReport;
	}

	public void setJasperReport(JasperReport jasperReport) {
		this.jasperReport = jasperReport;
	}

	public String getBaseDir() {
		return baseDir;
	}

	public void setBaseDir(String baseDir) {
		this.baseDir = baseDir;
	}

	public String getChaveParticipante() {
		return chaveParticipante;
	}

	public void setChaveParticipante(String chaveParticipante) {
		this.chaveParticipante = chaveParticipante;
	}

	public String getChaveSolicitante() {
		return chaveSolicitante;
	}

	public void setChaveSolicitante(String chaveSolicitante) {
		this.chaveSolicitante = chaveSolicitante;
	}
	
	public String getDataPrazo() {
		return dataPrazo;
	}
	public void setDataPrazo(String dataPrazo) {
		this.dataPrazo = dataPrazo;
	}


}
